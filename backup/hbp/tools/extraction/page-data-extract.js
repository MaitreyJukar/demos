var count = 0;
var page = 0;

function appendLink() {
    $("body").append("<a id='downloader' style='display:none'></a>")
}

function downloadData(storageObj, name) {
    name = name || "page-data";
    var dataStr = "data:text/json;charset=utf-8," + encodeURIComponent(JSON.stringify(storageObj, null, 4));
    var dlAnchorElem = $('#downloader')[0];
    dlAnchorElem.setAttribute("href", dataStr);
    dlAnchorElem.setAttribute("download", name + ".json");
    dlAnchorElem.click();
}

function sendNextRequest() {
    if (count < pageLinks.dataLinks.length) {
        var url = pageLinks.dataLinks[count].path.replace(/\d+\/index\.html/, page + "/index.html");
        console.log(url);
        $.ajax({
            url
        }).done(function (response) {
            pageLinks.dataLinks[count].pages = pageLinks.dataLinks[count].pages || [];
            pageLinks.dataLinks[count].pages.push(response);
            page++;
            sendNextRequest();
        }).fail(function () {
            page = 0;
            count++;
            sendNextRequest();
        });
    } else {
        appendLink();
        downloadData(pageLinks);
    }
};

sendNextRequest();